package expressionsstatementscodeblocksmethods;

public class StatementsWhiteSpaceAndIndenting {
    public static void main(String[] args) {
        int myVariable = 50; // the entire line is a statement in Java
        System.out.println("Printed");
        myVariable++;
        myVariable--;
        System.out.println("This is a test"); // ; for close a line
        // you can put multiple statements in one line devided by ;, but it not recommanded, its easier to read if the code is well formatted(indentation). To see the logical flow of
        // the code. CTRL + ALT + L. Use white spaces before the operators to make it look nicer

    }
}
